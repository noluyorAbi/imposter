export function getClueForWord(word: string, language: string): string {
  const clue = WORD_CLUES[word];
  if (!clue) {
    console.warn(`No clue found for word: "${word}"`);
    return "Unknown";
  }

  switch (language) {
    case "de":
      return clue.de;
    case "tr":
      return clue.tr;
    default:
      return clue.en;
  }
}
